namespace CSharpAdvanced;

public class Flower
{
     private string name;
     public string Name
     {
          get { return name; }
          set { name = value; }
     }
     private string color;
     public string Color
     {
          get { return color; }
          set { color = value; }
     }
     private double cost;
     public double Cost
     {
          get { return cost; }
          set { cost = value; }
     }
     
}